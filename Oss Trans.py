# Транслитератор осетинского алфавита с кириллицы на латиницу

def ossetian_transliterate(text: str) -> str:
    translit_dict = {
    'а': 'a', 'æ': 'æ', 'ае': 'æ', 'б': 'b', 'в': 'v', 'г': 'g', 'гъ': 'h', 
    'е': 'e', 'д': 'd', 'з': 'z', 'дз': 'dz', 'дж': 'dž', 'и': 'i', 
    'й': 'j', 'к': 'k', 'къ': 'kh', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'пъ': 'ph', 'р': 'r', 'с': 's', 
    'т': 't', 'тъ': 'th', 'у': 'u',
    'ф': 'f', 'х': 'x', 'хъ': 'q', 'ц': 'c', 'цъ': 'ch', 
    'ч': 'č', 'ш': 's', 'щ': 's', 'ъ': "'", 'ы': 'y', 'ь': "'", 
    'э': 'e', 'ю': 'ju', 'я': 'ja',
    # Добавляем заглавные буквы
    'А': 'A', 'Æ': 'Æ', 'Ае': 'Æ', 'АЕ': 'Æ', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Гъ': 'H', 
    'Е': 'E', 'Д': 'D', 'З': 'Z', 'Дз': 'Dz', 'Дж': 'Dž', 'И': 'I', 
    'Й': 'J', 'К': 'K', 'Къ': 'Kh', 'Л': 'L', 'М': 'M',
    'Н': 'N', 'О': 'O', 'П': 'P', 'Пъ': 'Ph', 'Р': 'R', 'С': 'S', 
    'Т': 'T', 'Тъ': 'Th', 'У': 'U',
    'Ф': 'F', 'Х': 'X', 'Хъ': 'Q', 'Ц': 'C', 'Цъ': 'Ch', 
    'Ч': 'Č', 'Ш': 'S', 'Щ': 'S', 'Ъ': "'", 'Ы': 'Y', 'Ь': "'", 
    'Э': 'E', 'Ю': 'Ju', 'Я': 'Ja',
}

    result = []
    i = 0
    n = len(text)
    
    while i < n:
        # Проверяем двухбуквенные сочетания (если есть следующий символ)
        if i + 1 < n:
            two_chars = text[i] + text[i+1]
            if two_chars in translit_dict:
                result.append(translit_dict[two_chars])
                i += 2  # Пропускаем 2 символа
                continue
        
        # Если двухбуквенного сочетания нет, проверяем один символ
        if text[i] in translit_dict:
            result.append(translit_dict[text[i]])
        else:
            result.append(text[i])  # Оставляем как есть (цифры, пунктуация)
        i += 1
    
    return ''.join(result)
    
print(ossetian_transliterate("Джусойты Нафи"))
